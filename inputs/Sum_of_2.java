import java.util.*;

public class Sum_of_2 {
    public static void main(String args[]) {
        Scanner sc = new Scanner(System.in);
        System.out.println("please enter 2 number whose addition you want to print :");
        int a = sc.nextInt();
        int b = sc.nextInt();
        int sum = a + b;
        System.out.println("the addition of 2 no. you entered is : " + sum);
    }

}